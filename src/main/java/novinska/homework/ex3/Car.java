package novinska.homework.ex3;

public class Car {
    int year;
    double speed;
    int weigh;
    String color;

    public Car() {

    }

    public Car(int year) {
        this.year = year;
    }

    public Car(int year, double speed) {
        this.year = year;
        this.color = color;
    }

    public Car(int year, double speed, int weigh) {
        this.year = year;
        this.speed = speed;
        this.weigh = weigh;
    }

    public Car(int year, double speed, int weigh, String color) {
        this.year = year;
        this.speed = speed;
        this.weigh = weigh;
        this.color = color;
    }
}
